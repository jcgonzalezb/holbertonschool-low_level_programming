#include <stdio.h>
#include <stdlib.h>
#include "3-calc.h"

/**
 * op_add - function
 * @a: number
 * @b: number
 * Return: number
 */

int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - function
 * @a: number
 * @b: number
 * Return: number
 */

int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - function
 * @a: number
 * @b: number
 * Return: number
 */

int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - function
 * @a: number
 * @b: number
 * Return: number
 */

int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - function
 * @a: number
 * @b: number
 * Return: number
 */


int op_mod(int a, int b)
{
return (a % b);
}

